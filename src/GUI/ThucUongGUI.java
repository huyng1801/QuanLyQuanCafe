    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Mã thức uống");
        model.addColumn("Tên thức uống");
        model.addColumn("Loại");
        model.addColumn("Giá");
        List<ThucUongDTO> ds =  ThucUongBLL.layDanhSachThucUong();
        for (ThucUongDTO thucUong : ds) {
            Object[] rowData = {thucUong.getMaThucUong(), thucUong.getTenThucUong(), LoaiBLL.timLoaiTheoMa(thucUong.getMaLoai()).getTenLoai(), thucUong.getGia()};
            model.addRow(rowData);
        }
        clear();
    }
     private void loadComboBox() {
        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
        cboLoai.setModel(comboBoxModel);
        List<LoaiDTO> ds = LoaiBLL.layDanhSachLoai();
        for (LoaiDTO loai : ds) {
            comboBoxModel.addElement(loai.getTenLoai());
        }
    }
