    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String tenThucUong = txtTenThucUong.getText();
        String loai = cboLoai.getSelectedItem() != null ? cboLoai.getSelectedItem().toString(): "";
        String giaText = txtGia.getText();
        if (tenThucUong.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên thức uống!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
         if (loai.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn loại thức uống!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
       if (giaText.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giá!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
       long lGia = Long.parseLong(giaText);
        ThucUongDTO thucUongDTO = new ThucUongDTO();
        thucUongDTO.setTenThucUong(tenThucUong);
        thucUongDTO.setMaLoai(LoaiBLL.timLoaiTheoTen(loai).getMaLoai());
        thucUongDTO.setGia(lGia);
        if (ThucUongBLL.themThucUong(thucUongDTO)) {
            JOptionPane.showMessageDialog(this, "Thêm thức uống thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thức uống thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản cần sửa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String maThucUong = model.getValueAt(selectedRow, 0).toString();
        String tenThucUong = txtTenThucUong.getText();
        String loai = cboLoai.getSelectedItem() != null ? cboLoai.getSelectedItem().toString(): "";
        String giaText = txtGia.getText();
        if (tenThucUong.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên thức uống!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
         if (loai.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn loại thức uống!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
       if (giaText.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giá!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
       long lGia = Long.parseLong(giaText);
        ThucUongDTO thucUongDTO = new ThucUongDTO();
        thucUongDTO.setMaThucUong(Integer.parseInt(maThucUong));
        thucUongDTO.setTenThucUong(tenThucUong);
        thucUongDTO.setMaLoai(LoaiBLL.timLoaiTheoTen(loai).getMaLoai());
        thucUongDTO.setGia(lGia);

        if (ThucUongBLL.suaThucUong(thucUongDTO)) {
            JOptionPane.showMessageDialog(this, "Sửa thức uống thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thức uống thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn thức uống cần xóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String maThucUong = model.getValueAt(selectedRow, 0).toString();

        int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa thức uống này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {

            if (ThucUongBLL.xoaThucUong(Integer.parseInt(maThucUong))) {
                JOptionPane.showMessageDialog(this, "Xóa thức uống thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thức uống thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
