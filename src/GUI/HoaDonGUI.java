    private void layDanhSachBan() {
        jPanel1.setLayout(new GridLayout(4, 4));
         jPanel1.removeAll();
        banButtons = new ArrayList<>();
        List<BanDTO> ds = BanBLL.layDanhSachBan();
        for (BanDTO ban : ds) {
            JButton button = new JButton(ban.getTenBan());
            if ("Trống".equals(ban.getTrangThai())) {
                button.setBackground(Color.GREEN);
            } else {
                button.setBackground(Color.RED);
            }
            banButtons.add(button);

            button.addActionListener(e -> {
                loadDataToTable(ban.getMaBan());
                txtBan.setText(ban.getTenBan());
            });

        jPanel1.add(button);
        jPanel1.updateUI();
        jPanel1.revalidate();
        jPanel1.repaint();

        }
    }

    private void loadDataToTable(int maBan) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("ID");
        model.addColumn("Thức uống");
        model.addColumn("Số lượng");
        model.addColumn("Đơn giá");
         model.addColumn("Thành tiền");
        List<ChiTietHoaDonDTO> ds = BanBLL.layChiTietHoaDonChuaThanhToanCuaBan(maBan);
        long tongTien = 0;
        for (ChiTietHoaDonDTO chiTietHoaDon : ds) {
            long thanhTien = chiTietHoaDon.getSoLuong() * ThucUongBLL.timThucUongTheoMa(chiTietHoaDon.getMaThucUong()).getGia();
            tongTien += thanhTien;
            Object[] rowData = {chiTietHoaDon.getId(), ThucUongBLL.timThucUongTheoMa(chiTietHoaDon.getMaThucUong()).getTenThucUong(), chiTietHoaDon.getSoLuong(), ThucUongBLL.timThucUongTheoMa(chiTietHoaDon.getMaThucUong()).getGia(), thanhTien};
            model.addRow(rowData);
        }
        labelTongTien.setText("Tổng tiền: " + tongTien);

    }

