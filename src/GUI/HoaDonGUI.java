    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        String tenBan = txtBan.getText();
        if (tenBan.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bàn!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        HoaDonDTO ds = BanBLL.layHoaDonChuaThanhToanCuaBan(BanBLL.timBanTheoTen(tenBan).getMaBan());
        if (ds == null) {
            HoaDonDTO hoaDonDTO = new HoaDonDTO();
            hoaDonDTO.setMaBan(BanBLL.timBanTheoTen(tenBan).getMaBan());
            HoaDonBLL.themHoaDon(hoaDonDTO);
            BanBLL.capNhatTrangThai(BanBLL.timBanTheoTen(tenBan).getMaBan(), "Có người");
            layDanhSachBan();
            ChiTietHoaDonDTO chiTietHoaDonDTO = new ChiTietHoaDonDTO();
            chiTietHoaDonDTO.setMaHoaDon(BanBLL.layHoaDonChuaThanhToanCuaBan(BanBLL.timBanTheoTen(tenBan).getMaBan()).getMaHoaDon());
            int maThucUOng = ThucUongBLL.timThucUongTheoTen(cboThucUong.getSelectedItem().toString()).getMaThucUong();
            chiTietHoaDonDTO.setMaThucUong(maThucUOng);
            String soLuongText = txtSoLuong.getText();
            if (soLuongText.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int soLuong = Integer.parseInt(soLuongText);
            chiTietHoaDonDTO.setSoLuong(soLuong);
            if (ChiTietHoaDonBLL.themChiTietHoaDon(chiTietHoaDonDTO)) {
                JOptionPane.showMessageDialog(this, "Thêm chi tiết hóa đơn thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable(BanBLL.timBanTheoTen(tenBan).getMaBan());
                
            } else {
                JOptionPane.showMessageDialog(this, "Thêm chi tiết hóa đơn thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            ChiTietHoaDonDTO chiTietHoaDonDTO = new ChiTietHoaDonDTO();
            chiTietHoaDonDTO.setMaHoaDon(BanBLL.layHoaDonChuaThanhToanCuaBan(BanBLL.timBanTheoTen(tenBan).getMaBan()).getMaHoaDon());
            int maThucUOng = ThucUongBLL.timThucUongTheoTen(cboThucUong.getSelectedItem().toString()).getMaThucUong();
            chiTietHoaDonDTO.setMaThucUong(maThucUOng);
            String soLuongText = txtSoLuong.getText();
            if (soLuongText.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int soLuong = Integer.parseInt(soLuongText);
            chiTietHoaDonDTO.setSoLuong(soLuong);
            if (ChiTietHoaDonBLL.themChiTietHoaDon(chiTietHoaDonDTO)) {
                JOptionPane.showMessageDialog(this, "Thêm chi tiết hóa đơn thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable(BanBLL.timBanTheoTen(tenBan).getMaBan());
            } else {
                JOptionPane.showMessageDialog(this, "Thêm chi tiết hóa đơn thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
 // TODO add your handling code here:
 
        String tenBan = txtBan.getText();
        if (tenBan.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bàn!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản cần xóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String id = model.getValueAt(selectedRow, 0).toString();

        int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn chi tiết hóa đơn này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {

            if (ChiTietHoaDonBLL.xoaChiTietHoaDon(Integer.parseInt(id))) {
                JOptionPane.showMessageDialog(this, "Xóa chi tiết hóa đơn thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable(BanBLL.timBanTheoTen(tenBan).getMaBan());
            } else {
                JOptionPane.showMessageDialog(this, "Xóa chi tiết hóa đơn thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
